// Helper function to get all possible trait values
export const getAllTraitValues = () => {
  const allValues: { [key: string]: string[] } = {}
  
  Object.entries(COMPREHENSIVE_TRAITS).forEach(([category, subcategories]) => {
    allValues[category] = []
    Object.values(subcategories).forEach(values => {
      if (Array.isArray(values)) {
        allValues[category].push(...values)
      }
    })
  })
  
  return allValues
}

// Helper function to search for traits by keyword
export const searchTraits = (keyword: string) => {
  const results: { [key: string]: string[] } = {}
  const keywordLower = keyword.toLowerCase()
  
  Object.entries(COMPREHENSIVE_TRAITS).forEach(([category, subcategories]) => {
    results[category] = []
    Object.values(subcategories).forEach(values => {
      if (Array.isArray(values)) {
        const matches = values.filter(value => 
          value.toLowerCase().includes(keywordLower)
        )
        results[category].push(...matches)
      }
    })
  })
  
  return results
}

// Helper function to get random traits
export const getRandomTraits = () => {
  const traits: { [key: string]: string } = {}
  
  Object.entries(COMPREHENSIVE_TRAITS).forEach(([category, subcategories]) => {
    const allValues: string[] = []
    Object.values(subcategories).forEach(values => {
      if (Array.isArray(values)) {
        allValues.push(...values)
      }
    })
    
    if (allValues.length > 0) {
      traits[category] = allValues[Math.floor(Math.random() * allValues.length)]
    }
  })
  
  return traits
}

